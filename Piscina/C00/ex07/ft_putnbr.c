#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;

	if (nb <= 0)
	{
		nb = -nb;
		write(1, "-", 1);
	}
	if (nb / 10 == 0)
	{
		c = nb + '0';
		write(1, &c, 1);
	}
	else
	{
		c = nb % 10 + '0';
		ft_putnbr(nb / 10);
		write(1, &c, 1);
	}
}
